/**
 * Echo a variable. If string, use `<pre> to output without char limit. If object, dump both values and methods.
 * 
 * @param mixed $value
 * @since 6.2.1
 */
function px_var_dump($value) {
  if (is_string($value)) {
    $string = htmlentities(print_r($value, true));
    echo "<pre>{$string}</pre>";
  }
  elseif (is_object($value)) {
    var_dump($value);
    var_dump(get_class_methods($value));
  }
  else {
    var_dump($value);
  }
}

/**
 * Do a GET request.
 * 
 * Usage:  
 * `H::GET( 'https://yoursite.com/wp-json/my/v1/get-endpoint' );`
 * 
 * Shorthand: (need to define base url in `API_URL` constant)
 * `H::GET( '/get-endpoint' );`
 */
function h_GET(string $url, $data = []) {
  // if URL doesn't start with "http", prepend API_URL
  if (!preg_match('/^http/', $url, $matches)) {
    $url = API_URL . $url;
  }

  if ($data) {
    $url = sprintf("%s?%s", $url, http_build_query($data));
  }
  
  $curl = curl_init();
  curl_setopt($curl, CURLOPT_URL, $url);
  curl_setopt($curl, CURLOPT_RETURNTRANSFER, 1);

  $result = curl_exec($curl);
  curl_close($curl);

  return json_decode($result, true);
}


/**
 * Do a POST request.
 * 
 * Usage:  
 * `H::POST( 'https://yoursite.com/wp-json/my/v1/post-endpoint', [ 'param1' => 'value1' ] );`
 * 
 * Shorthand: (need to define base url in `API_URL` constant)  
 * `H::POST( '/post-endpoint', [ 'param1' => 'value1' ] );`
 */
function h_POST(string $url, $data = []) {
  // if URL doesn't start with "http", prepend API_URL
  if (!preg_match('/^http/', $url, $matches)) {
    $url = API_URL . $url;
  }

  $payload = json_encode($data);

  // Prepare new cURL resource
  $ch = curl_init($url);
  curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
  curl_setopt($ch, CURLINFO_HEADER_OUT, true);
  curl_setopt($ch, CURLOPT_POST, true);
  curl_setopt($ch, CURLOPT_POSTFIELDS, $payload);
  
  // Set HTTP Header for POST request 
  curl_setopt(
    $ch,
    CURLOPT_HTTPHEADER,
    [
      'Content-Type: application/json',
      'Content-Length: ' . strlen($payload)
    ]
  );
  
  // Submit the POST request
  $response = curl_exec($ch);
  curl_close($ch);

  return $response;
}


